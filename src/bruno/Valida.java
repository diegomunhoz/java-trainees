package bruno;

/**
 * Classe para validar se os valores informados são válidos
 *
 * @author Bruno Ferreira Mariani
 * @since 24/02/2021
 */
public class Valida {
	
	// Método para checar se o número é menor do que 1
	// True caso seja maior ou igual a 1, false caso contrário
	public static boolean isLessThanOne(int args){
		return (args >= 1);
	}
	
}
